package asciistudio;

/**
 * Stores data on the progress of a render.
 *
 * @author Ian Martinez
 */
public class RenderProgress {

    private String stage;
    private int progress;
    private int max;

    /**
     * Create a new RenderProgress.
     *
     * @param stage what stage this progress is a part of
     * @param progress the current progress value
     * @param max the max progress value
     */
    public RenderProgress(String stage, int progress, int max) {
        this.stage = stage;
        this.progress = progress;
        this.max = max;
    }

    /**
     * @return the stage
     */
    public String getStage() {
        return stage;
    }

    /**
     * @param stage the stage to set
     */
    public void setStage(String stage) {
        this.stage = stage;
    }

    /**
     * @return the progress
     */
    public int getProgress() {
        return progress;
    }

    /**
     * @param progress the progress to set
     */
    public void setProgress(int progress) {
        this.progress = progress;
    }

    /**
     * @return the max
     */
    public int getMax() {
        return max;
    }

    /**
     * @param max the max to set
     */
    public void setMax(int max) {
        this.max = max;
    }
    
}
